	<h1>AVISO DE PRIVACIDAD SPERT S.A. de C.V.</h1>
		<p>	
			Su privacidad y confianza son muy importantes para <strong>Servicios Profesionales en Recursos Humanos y Tecnologías S.A. de C.V.</strong>,
			así como para todos nuestros proveedores y socios estratégicos. Por ello, queremos asegurarnos de que conozca cómo 
			salvaguardamos la integridad, privacidad y protección de sus datos personales, en apego a la Ley Federal de Protección de 
			Datos Personales en Posesión de los Particulares, publicada en el Diario Oficial de la Federación el día 5 de julio de 2010.
		</p><br />
		<p>
			Para nosotros, la recopilación de ciertos datos personales es necesaria para llevar a cabo las actividades intrínsecas a 
			nuestra actividad mercantil y tenemos la obligación de cumplir con las medidas legales y de seguridad suficientes para 
			proteger aquellos datos personales recabados para las finalidades que se describen en el presente aviso de privacidad.
		</p><br />
		<p>
			Con el objetivo de que usted tenga pleno control y decisión sobre sus datos personales, le recomendamos leer atentamente la siguiente información.
			Usted tendrá disponible en todo momento este aviso de privacidad en nuestra página de Internet: <a href="http://www.spert.com.mx">www.spert.com.mx</a>.
			Se entenderá que usted como titular consiente tácitamente el tratamiento de sus datos, cuando habiéndose puesto a su disposición este aviso de privacidad,
			no manifieste su oposición.
		</p><br />
		<ol>
			<li value="1"><strong>Identidad y domicilio de cada responsable:</strong></li> 
				<p>
				<strong>Servicios Profesionales en Recursos Humanos y Tecnologías S.A. de C.V.</strong>, así como aquellos proveedores y socios estratégicos afiliadas bajo el control 
				común se identifican a continuación, quienes para los efectos de este aviso de privacidad tienen la calidad de responsables frente a usted, como titular de datos personales:
				</p><br />
				<p>
					<strong>
						Servicios Profesionales en Recursos Humanos y Tecnologías S.A. de C.V. <br />
						Calzada Ticomán No. 146, Col. Lindavista, C.P. 07300 Gustavo A. Madero, México D.F.
					</strong>
				</p><br />
			 <li><strong>Principio de información:</strong></li>
			 	<p>
				Cada responsable le informa que posee, recaba o recabará de usted, los datos personales necesarios, principalmente para la adecuada realización 
				de las operaciones y prestación de los servicios, así como para la celebración de los demás actos que cada responsable puede realizar conforme a la ley y sus estatutos sociales. 
				Dichos datos personales pueden haber sido o pueden ser obtenidos de usted, ya sea personalmente o bien, directamente por cualquier medio electrónico, óptico, sonoro, visual,
				o a través de cualquier otra tecnología. Asimismo, podemos obtener datos personales de los que usted es titular, a través de terceros y de otras fuentes permitidas por la ley, 
				tales como las sociedades de información crediticia.
				</p><br />
				<p>Los datos personales recabados o que se recaban podrán incluir, entre otros, los siguientes:</p>
					<ol style="list-style-type:upper-roman">
						<li>Datos de identificación y de contacto: nombre completo, dirección, teléfono de casa, celular y/o de trabajo.</li>
					</ol><br />

			<li><strong>Finalidades del tratamiento de datos personales:</strong></li>
				<p>Las responsables harán tratamiento de los datos personales recabados, así como sensibles, en su caso, para los siguientes fines:</p><br />
			<ol style="list-style-type:upper-roman"> 
				<li>La realización de todas y cada una de las operaciones y la prestación de los servicios, así como la celebración de los demás actos que cada responsable
				puede realizar conforme a la ley y sus estatutos sociales.</li>
			 	<li>La realización de encuestas, así como la creación e implementación de procesos analíticos y estadísticos necesarios o convenientes, relacionados con dichas operaciones, servicios y actos.</li>
			 	<li>La promoción de productos, servicios, beneficios adicionales, descuentos, promociones.</li>
			 	<li>La atención de requerimientos de cualquier autoridad competente.</li>
			 	<li>La realización de cualquier actividad complementaria o auxiliar necesaria para la realización de los fines anteriores.</li>
		 		<li>La puesta en contacto con usted para tratar cualquier tema relacionado con sus datos personales o con el presente aviso de privacidad.</li>
			</ol><br />

				<p>El tratamiento de datos personales se limitará:</p> 
				<ol style="list-style-type:upper-roman"> 
					<li>Al cumplimiento de las finalidades previstas en este aviso de privacidad.</li>
					<li>A fines distintos que resulten compatibles o análogos a los establecidos en este aviso de privacidad,
					sin que para ello se requiera obtener nuevamente, en su caso, el consentimiento del titular.
					Asimismo, el tratamiento de datos personales será el que resulte necesario, adecuado y relevante en relación con las finalidades previstas 
					en este aviso de privacidad, así como con los fines distintos que resulten compatibles o análogos.</li>
				</ol><br />

		 	<li><strong>Transferencia de datos:</strong></li>
		 		<p>
				Para cumplir con las finalidades previstas en este aviso de privacidad, no se requerirá efectuar transferencia de los datos personales a entidades o personas distintas
				a nuestros socios comerciales.
				</p><br />
				
				<p>En virtud de este aviso de privacidad, el titular acepta la transferencia de sus datos, en el entendido que:</p>
				<ol style="list-style-type:upper-roman"> 
					<li>Cada responsable pretende transferir los datos personales a terceros nacionales o extranjeros, distintos del encargado, a quienes dicho responsable comunicará 
						este aviso de privacidad y las finalidades a las que el titular sujetó su tratamiento.</li> 
					<li>El tercero receptor, asumirá las mismas obligaciones que correspondan al responsable que transfirió los datos, por lo que el tratamiento de los datos se hará conforme 
						a lo convenido en este aviso de privacidad.</li>
				</ol><br />
				<p>
				(f) Profesionistas, asesores o consultores externos, para efecto de la administración de las operaciones, de los servicios y de los demás actos que cada responsable 
				puede realizar conforme a la ley y sus estatutos sociales, así como para la defensa de los intereses de cualquier responsable ante cualquier controversia legal que 
				surja con motivo de dichas operaciones y servicios, tales como agencias de cobranza o auditores externos, legales y contables.
				</p><br />

			<li><strong>Departamento de datos personales:</strong></li>
		        <p>
		        Las responsables designan como personas o departamentos de datos personales, quienes darán trámite a las solicitudes de los titulares, para el ejercicio de los derechos a que se 
		        refiere la Ley Federal de Protección de Datos Personales en Posesión de Particulares (acceso, rectificación, cancelación y oposición, así como limitación al el uso o divulgación 
		        de los datos y revocación del consentimiento), para este efecto se considerará como “Departamento de datos personales”.
		    	</p><br />

			<li><strong>Medios para ejercer los derechos de acceso, rectificación, cancelación y oposición:</strong></li>
				<p>
		        Sujeto a lo dispuesto en la Ley Federal de Protección de Datos Personales en Posesión de Particulares, a partir del 6 de Enero de 2011, el titular por sí o mediante representante legal debidamente acreditado, 
		        podrá ejercer sus derechos de acceso, rectificación, cancelación y oposición, formulando la solicitud respectiva únicamente a través de las unidades y los medios de atención de la responsable de que se trate, 
		        designados como “Departamento de datos personales” en este aviso de privacidad, a través del número telefónico (55) 63950125 y el correo electrónico <b>contacto.df@spert.com.mx</b>.
		        </p>

				<p>Para su conocimiento, cada uno de los referidos derechos, respectivamente le permite:</p>
				<ol style="list-style-type:upper-roman"> 
					<li>Acceder a sus datos personales que obren en poder del responsable, así como conocer el Aviso de Privacidad al que está sujeto el tratamiento. La responsable de que se trate pondrá a su disposición los datos personales, 
						o bien expedirá copias simples, documentos electrónicos o cualquier otro medio. La entrega de los datos personales será gratuita, debiendo cubrir el titular únicamente los gastos justificados de envío o con el costo de 
						reproducción en copias u otros formatos. Dicho derecho se ejercerá por el titular en forma gratuita, previa acreditación de su identidad ante el responsable de que se trate. No obstante, si el titular reitera su solicitud 
						en un periodo menor a doce meses, los costos no serán mayores a tres días de Salario Mínimo General Vigente en el Distrito Federal a menos que existan modificaciones sustanciales al aviso de privacidad que motiven nuevas consultas.
					</li>
					<li>Rectificar cualquiera de sus datos personales que sea inexacto o incompleto.</li>
					<li>Cancelar sus datos personales. En caso de ser procedente la solicitud, los datos personales entrarán al período de bloqueo en el cual no podrán ser tratados. Una vez cancelado el dato se dará aviso a su titular.</li>
					<li>Oponerse en todo momento y por causa legítima al tratamiento de sus datos personales.</li>
				</ol><br />

			<li><strong>Opciones y medios para ejercer los derechos de limitar el uso o divulgación de los datos y mecanismos y procedimientos para revocar el consentimiento:</strong></li>
		        <p>Usted también podrá en cualquier momento:</p> 
		        <ol style="list-style-type:upper-roman"> 
		        	<li>Optar por limitar el uso o divulgación de sus datos personales.</li>
		        	<li>Revocar su consentimiento para dar tratamiento a sus datos personales, en la inteligencia que todo tratamiento de datos personales estará sujeto al consentimiento de su titular, salvo las excepciones previstas 
		        		por la Ley Federal de Protección de Datos Personales en Posesión de Particulares.
		        	</li>
		        </ol><br />
		        <p>
				Para ejercer cualquiera de dichos derechos, usted deberá formular la solicitud respectiva únicamente a través de las unidades y los medios de atención de la responsable de que se trate, designados como 
				“Departamento de datos personales” en este aviso de privacidad, a través de los medios, escritos, electrónicos o de otro tipo y conforme al procedimiento que dicho departamento de datos personales tenga establecido al efecto.
				<p><br />

			<li><strong>Procedimiento y medio por el cual se comunicará de cambios al aviso de privacidad:</strong></li>
		        <p>
		        Nos reservamos el derecho de efectuar en cualquier momento cambios o modificaciones al presente aviso de privacidad, ya sea para la atención de novedades legislativas, regulatorias o jurisprudenciales, políticas internas, 
		        prácticas del mercado o por cualquier otra razón. Cualquier cambio que se realice a este aviso de privacidad, será incorporado al mismo por las responsables y será dado a conocer a través de cualquiera de los siguientes medios:
				</p>
				<br />
		        <ul style="list-style-type: circle;"> 
		        	<li>Nuestro centro de atención a clientes.</li>
		        	<li>Nuestra página de internet: <a href="http://www.spert.com.mx">www.spert.com.mx</a></li>
		        </ul>
